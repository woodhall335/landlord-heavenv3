function formatBedroomDescriptor(bedrooms?: number | null): string {
  if (!bedrooms || bedrooms <= 0) {
    return '';
  }

  const words: Record<number, string> = {
    1: 'one-bedroom',
    2: 'two-bedroom',
    3: 'three-bedroom',
    4: 'four-bedroom',
    5: 'five-bedroom',
  };

  return words[bedrooms] || `${bedrooms}-bedroom`;
}

function pluralizeComparablePropertyType(propertyType: string, count: number): string {
  const trimmed = propertyType.trim();
  if (!trimmed) {
    return count === 1 ? 'home' : 'homes';
  }

  if (count === 1 || trimmed.endsWith('s')) {
    return trimmed;
  }

  return `${trimmed}s`;
}

function buildComparableGrounding(
  state: Section13State,
  comparables: Section13Comparable[],
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  const comparableCount = preview?.comparableCount ?? comparables.length;
  if (comparableCount <= 0) {
    return 'the recorded comparable evidence available for this tenancy';
  }

  const sourceBackedCount = preview?.sourceBackedCount ?? 0;
  const freshComparableCount = preview?.freshComparableCount ?? 0;
  const bedroomDescriptor = formatBedroomDescriptor(
    state.comparablesMeta.bedrooms || state.tenancy.bedrooms || null
  );
  const propertyTypes = Array.from(
    new Set(
      comparables
        .map((item) => item.propertyType?.trim().toLowerCase())
        .filter((value): value is string => Boolean(value))
    )
  );
  const propertyDescriptor =
    propertyTypes.length === 1
      ? pluralizeComparablePropertyType(propertyTypes[0], comparableCount)
      : 'properties';
  const descriptor = bedroomDescriptor ? `${bedroomDescriptor} ${propertyDescriptor}` : propertyDescriptor;

  const freshnessFragment =
    freshComparableCount === comparableCount
      ? 'all listed within the last 90 days'
      : freshComparableCount > 0
        ? `${freshComparableCount} listed within the last 90 days`
        : 'no dated listings within the 90-day freshness window';
  const sourceBackedFragment =
    sourceBackedCount === comparableCount
      ? 'all source-backed'
      : sourceBackedCount > 0
        ? `${sourceBackedCount} source-backed`
        : 'no source-backed entries';

  return `${comparableCount} comparable ${descriptor} within 0.5 miles, with ${freshnessFragment} and ${sourceBackedFragment}`;
}

function buildComparableGroundingSentence(
  state: Section13State,
  comparables: Section13Comparable[],
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  const comparableCount = preview?.comparableCount ?? comparables.length;

  if (comparableCount <= 0) {
    return 'Comparable analysis is not complete enough yet to describe the local market evidence base.';
  }

  const sourceBackedCount = preview?.sourceBackedCount ?? 0;
  const freshComparableCount = preview?.freshComparableCount ?? 0;
  const bedroomDescriptor = formatBedroomDescriptor(
    state.comparablesMeta.bedrooms || state.tenancy.bedrooms || null
  );
  const descriptor = bedroomDescriptor ? `${bedroomDescriptor} homes` : 'homes';

  const freshnessFragment =
    freshComparableCount === comparableCount
      ? 'all listed within the last 90 days'
      : freshComparableCount > 0
        ? `${freshComparableCount} listed within the last 90 days`
        : 'with no dated listings inside the last 90 days';

  const sourceBackedFragment =
    sourceBackedCount === comparableCount
      ? 'all source-backed'
      : sourceBackedCount > 0
        ? `${sourceBackedCount} source-backed`
        : 'no source-backed entries';

  if (freshComparableCount === comparableCount && sourceBackedCount === comparableCount) {
    return `This proposal is based on ${comparableCount} comparable ${descriptor} within 0.5 miles, all listed within the last 90 days and all source-backed.`;
  }

  return `This proposal is based on ${comparableCount} comparable ${descriptor} within 0.5 miles, with ${freshnessFragment} and ${sourceBackedFragment}.`;
}

function getMedianPositionFragment(
  state: Section13State,
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  if (!preview || preview.proposedRentMonthly == null || preview.median == null) {
    return 'without a settled median position yet';
  }

  if (preview.proposedRentMonthly < preview.median) {
    return `below the adjusted median of ${formatCurrency(preview.median)} pcm`;
  }

  if (preview.proposedRentMonthly > preview.median) {
    return `above the adjusted median of ${formatCurrency(preview.median)} pcm`;
  }

  return `at the adjusted median of ${formatCurrency(preview.median)} pcm`;
}

function getCasePositionVariants(context: {
  state: Section13State;
  comparables?: Section13Comparable[];
  snapshot?: Section13OutputSnapshot | null;
}): {
  cover: string;
  justification: string;
  argument: string;
  defensive: string;
} {
  const { state, snapshot } = context;
  const preview = getResolvedPreview(state, snapshot);
  const comparables = context.comparables || [];
  const proposedRent = `${formatCurrency(state.proposal.proposedRentAmount)} ${frequencyLabel(
    state.tenancy.currentRentFrequency
  )}`;

  if (!preview) {
    return {
      cover:
        'The proposed increase can be turned into a service-ready notice pack once the comparable analysis and validation checks are complete.',
      justification:
        'The recorded facts can be analysed properly once the comparable evidence and validation checks are complete.',
      argument:
        'The proposed rent can only be defended once the comparable analysis and validation checks are complete on the saved facts.',
      defensive:
        'The landlord position should not be advanced until the comparable analysis and validation checks are complete on the saved facts.',
    };
  }

  const grounding = buildComparableGrounding(state, comparables, snapshot);
  const positionFragment = getMedianPositionFragment(state, snapshot);

  if (!preview.enteredStartDateValid || preview.validationIssues.length > 0) {
    return {
      cover:
        'The proposed increase already has recorded supporting evidence, but the saved facts still need validation issues resolved before service.',
      justification:
        `The file already records ${grounding}, but the saved facts still need validation issues resolved before the proposal can be relied on.`,
      argument:
        `The proposed rent of ${proposedRent} already has ${grounding} behind it, but the landlord should resolve the saved validation issues before advancing the case.`,
      defensive:
        `The landlord position remains anchored to ${grounding}, but the file should only be advanced once the recorded validation issues have been resolved.`,
    };
  }

  return {
    cover: `The saved evidence base of ${grounding} places the proposed rent ${positionFragment} and makes the notice ready to serve on the recorded facts.`,
    justification: `The recorded comparables place the proposed rent ${positionFragment} and support it as a market-aligned figure for this tenancy.`,
    argument: `The proposed rent of ${proposedRent} is supported by ${grounding} and is positioned ${positionFragment}, making it a reasonable and defensible figure under section 13.`,
    defensive: `The landlord's position is that ${grounding} supports a proposed rent of ${proposedRent}, which remains ${positionFragment} and should therefore be judged against the recorded comparable market evidence rather than the size of the increase alone.`,
  };
}

function buildConclusionSentence(
  state: Section13State,
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);

  if (!preview) {
    return 'The proposed rent can be concluded on once the comparable analysis is complete.';
  }

  if (!preview.enteredStartDateValid || preview.validationIssues.length > 0) {
    return 'The proposed rent has supporting market evidence, but the notice file still needs the recorded validation issues resolved before service.';
  }

  if (
    preview.proposedRentMonthly == null ||
    preview.lowerQuartile == null ||
    preview.median == null ||
    preview.upperQuartile == null
  ) {
    return 'The proposed rent is supported by current market evidence and remains tied to the recorded comparable file.';
  }

  if (preview.proposedRentMonthly <= preview.lowerQuartile) {
    return 'The proposed rent is supported by current market evidence and sits within the lower end of comparable properties locally.';
  }

  if (preview.proposedRentMonthly <= preview.median) {
    return 'The proposed rent is supported by current market evidence and sits within the lower-to-mid range of comparable properties locally.';
  }

  if (preview.proposedRentMonthly <= preview.upperQuartile) {
    return 'The proposed rent is supported by current market evidence and sits within the mid-to-upper range of comparable properties locally.';
  }

  return 'The proposed rent is supported by current market evidence and sits above the upper quartile of comparable properties locally.';
}

function buildPackAnchorSentence(
  state: Section13State,
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  if (!preview) {
    return 'The pack is ready to record the landlord’s proposed increase once the comparable analysis and validation checks are complete.';
  }

  if (!preview.enteredStartDateValid || preview.validationIssues.length > 0) {
    return 'The pack records the proposed increase and supporting market evidence, but the saved facts still show issues to resolve before service.';
  }

  const supportedByCount =
    preview.freshComparableCount > 0
      ? preview.freshComparableCount
      : preview.sourceBackedCount > 0
        ? preview.sourceBackedCount
        : preview.comparableCount;

  const supportLabel =
    preview.freshComparableCount > 0
      ? 'recent source-backed comparables'
      : preview.sourceBackedCount > 0
        ? 'source-backed comparables'
        : 'recorded comparables';

  let medianPosition = 'keeps the proposed rent anchored to the adjusted local market evidence';
  if (preview.proposedRentMonthly != null && preview.median != null) {
    medianPosition =
      preview.proposedRentMonthly < preview.median
        ? 'places the proposed rent below the adjusted local median'
        : preview.proposedRentMonthly > preview.median
          ? 'places the proposed rent above the adjusted local median'
          : 'places the proposed rent at the adjusted local median';
  }

  return `This notice pack is ready for service on the saved facts, is supported by ${supportedByCount} ${supportLabel}, and ${medianPosition}.`;
}

function buildMarketPositionSentence(
  state: Section13State,
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  if (
    !preview ||
    preview.proposedRentMonthly == null ||
    preview.lowerQuartile == null ||
    preview.median == null ||
    preview.upperQuartile == null
  ) {
    return 'Comparable analysis is not complete enough to place the proposed rent within an adjusted market range.';
  }

  let positionText = 'within the adjusted comparable range';
  if (preview.proposedRentMonthly <= preview.lowerQuartile) {
    positionText = 'at or below the lower quartile of the adjusted comparable range';
  } else if (preview.proposedRentMonthly <= preview.median) {
    positionText = 'between the lower quartile and the median of the adjusted comparable range';
  } else if (preview.proposedRentMonthly <= preview.upperQuartile) {
    positionText = 'between the median and the upper quartile of the adjusted comparable range';
  } else {
    positionText = 'above the upper quartile of the adjusted comparable range';
  }

  return `The proposed rent of ${formatCurrency(state.proposal.proposedRentAmount)} ${frequencyLabel(state.tenancy.currentRentFrequency)} sits ${positionText}, against an adjusted range of ${formatCurrency(preview.lowerQuartile)} to ${formatCurrency(preview.upperQuartile)} pcm and a median of ${formatCurrency(preview.median)} pcm.`;
}

function buildEvidenceStrengthSentence(
  state: Section13State,
  comparables: Section13Comparable[],
  snapshot?: Section13OutputSnapshot | null
): string {
  const preview = getResolvedPreview(state, snapshot);
  if (!preview) {
    return 'Evidence strength cannot be described until comparable data has been added.';
  }

  const base = `${preview.evidenceBandLabel} is based on ${preview.comparableCount} comparable home${preview.comparableCount === 1 ? '' : 's'}, ${preview.sourceBackedCount} source-backed entr${preview.sourceBackedCount === 1 ? 'y' : 'ies'}, and ${preview.freshComparableCount} comparables dated within the 90-day freshness window.`;

  if (preview.evidenceBand === 'strong') {
    return `${base} The file reads as a well-supported market review rather than a bare assertion.`;
  }

  if (preview.evidenceBand === 'moderate') {
    return `${base} The proposal is supportable, but the landlord should stay disciplined and rely on the recorded adjustments rather than broad statements about the market.`;
  }

  const hasLinkedEvidence = comparables.some((item) => item.sourceUrl);
  return `${base} The landlord should expect closer scrutiny, so the explanation should stay anchored to the current comparables${hasLinkedEvidence ? ' and saved listing links' : ''}.`;
}

