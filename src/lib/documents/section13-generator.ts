function safeText(value: string | number | null | undefined): string {
  if (value === null || value === undefined) return '';
  return toWinAnsiSafeText(String(value));
}

function formatCurrency(value: number | string | null | undefined): string {
  if (value === null || value === undefined || value === '') {
    return 'nil';
  }

  const numeric = typeof value === 'number' ? value : Number(String(value).replace(/[^\d.-]/g, ''));
  if (Number.isNaN(numeric)) {
    return String(value);
  }

  return `£${numeric.toFixed(2)}`;
}

function formatCurrencyFieldValue(value: number | string | null | undefined): string {
  if (value === null || value === undefined || value === '') {
    return 'nil';
  }

  const numeric = typeof value === 'number' ? value : Number(String(value).replace(/[^\d.-]/g, ''));
  if (Number.isNaN(numeric)) {
    return String(value);
  }

  return numeric.toFixed(2);
}

function formatDateUk(dateString?: string | null): string {
  if (!dateString) return '';
  const date = new Date(`${dateString}T00:00:00.000Z`);
  if (Number.isNaN(date.getTime())) return dateString;
  return new Intl.DateTimeFormat('en-GB', {
    day: '2-digit',
    month: 'long',
    year: 'numeric',
    timeZone: 'UTC',
  }).format(date);
}

function splitDate(dateString?: string | null): { day: string; month: string; year: string } {
  if (!dateString) return { day: '', month: '', year: '' };
  const [year = '', month = '', day = ''] = dateString.split('T')[0].split('-');
  return { day, month, year };
}

function formatCombDateValue(dateString?: string | null): string {
  const { day, month, year } = splitDate(dateString);
  return `${day}${month}${year}`;
}

