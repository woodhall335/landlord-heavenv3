  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.landlordPostcode, landlordPostcode);
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.landlordPhone, resolvedState.landlord.landlordPhone || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.landlordEmail, resolvedState.landlord.landlordEmail || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentName, resolvedState.landlord.agentName || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentAddressLine1, resolvedState.landlord.agentAddressLine1 || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentAddressLine2, resolvedState.landlord.agentAddressLine2 || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentTownCity, resolvedState.landlord.agentTownCity || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentCounty, '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentPostcode, agentPostcode);
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentPhone, resolvedState.landlord.agentPhone || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.agentEmail, resolvedState.landlord.agentEmail || '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.currentRentAmount, formatCurrencyFieldValue(resolvedState.tenancy.currentRentAmount));
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.currentRentFrequency, toTitleFrequency(resolvedState.tenancy.currentRentFrequency));
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.tenancyStartDate, formatCombDateValue(startDate));
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.lastIncreaseDate, formatCombDateValue(lastIncreaseDate));
