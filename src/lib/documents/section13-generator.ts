async function createBundleIndexPdf(artifacts: BundleArtifact[]): Promise<Uint8Array> {
  const lines = artifacts
    .filter((item) => item.includeInMerged)
    .map((item) => `${item.exhibitLabel ? `${item.exhibitLabel}: ` : ''}${item.title}${item.pageCount ? ` (${item.pageCount} page${item.pageCount === 1 ? '' : 's'})` : ''}`);

  return createNarrativePdf(
    'Tribunal Bundle Index',
    [{ lines }],
    'Section 13 Defensive Pack | Bundle index'
  );
}

async function mergePdfArtifacts(
  coverBytes: Uint8Array,
  indexBytes: Uint8Array,
  generatedDocs: Section13BundleSourceDocument[],
  evidenceFiles: Section13EvidenceFile[]
): Promise<{
  bundleBytes: Uint8Array;
  bundleArtifacts: BundleArtifact[];
  warnings: string[];
}> {
  const warnings: string[] = [];
  const bundlePdf = await PDFDocument.create();
  const artifacts: BundleArtifact[] = [];

  const addPdf = async (
    title: string,
    bytes: Uint8Array,
    logicalSection: Section13BundleAsset['logicalSection'],
    assetType: Section13BundleAsset['assetType'],
    sourceKind: Section13BundleAsset['sourceKind'],
    includeInMerged: boolean,
    extra?: Partial<BundleArtifact>
  ) => {
    const source = await PDFDocument.load(bytes);
    const copiedPages = await bundlePdf.copyPages(source, source.getPageIndices());
    copiedPages.forEach((page) => bundlePdf.addPage(page));
    artifacts.push({
      title,
      logicalSection,
      assetType,
      sourceKind,
      includeInMerged,
      pageCount: source.getPageCount(),
      status: 'generated',
      ...extra,
    });
  };

  await addPdf('Bundle cover sheet', coverBytes, 'cover', 'report', 'supporting_input', true);
  await addPdf('Bundle index', indexBytes, 'index', 'report', 'supporting_input', true);

  for (const doc of generatedDocs) {
    const logicalSection =
      doc.document_type === 'section13_form_4a' ? 'core_notice' : 'supporting_report';
    const assetType =
      doc.document_type === 'section13_form_4a' ? 'form' : 'report';
    await addPdf(doc.title, doc.pdf, logicalSection, assetType, 'generated_document', true, {
      documentType: doc.document_type,
      sourceDocumentId: doc.sourceDocumentId || null,
    });
  }

  for (const evidence of evidenceFiles) {
    try {
      if (evidence.contentType === 'application/pdf') {
        await addPdf(
          evidence.upload.title || evidence.upload.fileName,
          evidence.bytes,
          'uploaded_evidence',
          'evidence',
          'uploaded_evidence',
          true,
          {
            sourceUploadId: evidence.upload.id || null,
            exhibitLabel: evidence.upload.exhibitLabel || null,
          }
        );
        continue;
      }

      if (evidence.contentType === 'image/png' || evidence.contentType === 'image/jpeg') {
        const imageDoc = await PDFDocument.create();
        const page = imageDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
        const image: PDFImage =
          evidence.contentType === 'image/png'
            ? await imageDoc.embedPng(evidence.bytes)
            : await imageDoc.embedJpg(evidence.bytes);
        const bold = await imageDoc.embedFont(StandardFonts.HelveticaBold);
        const scale = Math.min((PAGE_WIDTH - 80) / image.width, (PAGE_HEIGHT - 120) / image.height);
        const width = image.width * scale;
        const height = image.height * scale;
        page.drawText(safeText(evidence.upload.title || evidence.upload.fileName), {
          x: 40,
          y: PAGE_HEIGHT - 40,
          size: 12,
          font: bold,
          color: rgb(0.16, 0.16, 0.16),
        });
        page.drawImage(image, {
          x: (PAGE_WIDTH - width) / 2,
          y: (PAGE_HEIGHT - height) / 2 - 20,
          width,
          height,
        });
        const imageBytes = await imageDoc.save();
        await addPdf(
          evidence.upload.title || evidence.upload.fileName,
          imageBytes,
          'uploaded_evidence',
          'evidence',
          'uploaded_evidence',
          true,
          {
            sourceUploadId: evidence.upload.id || null,
            exhibitLabel: evidence.upload.exhibitLabel || null,
          }
        );
        continue;
      }

      warnings.push(`Skipped unsupported evidence file type for ${evidence.upload.fileName}.`);
      artifacts.push({
        title: evidence.upload.title || evidence.upload.fileName,
        logicalSection: 'uploaded_evidence',
        assetType: 'evidence',
        sourceKind: 'uploaded_evidence',
        includeInMerged: false,
        status: 'warning',
        generationError: 'Unsupported evidence MIME type',
        sourceUploadId: evidence.upload.id || null,
      });
    } catch (error: any) {
      warnings.push(`Could not include uploaded evidence "${evidence.upload.fileName}" in the merged bundle.`);
      artifacts.push({
        title: evidence.upload.title || evidence.upload.fileName,
        logicalSection: 'uploaded_evidence',
        assetType: 'evidence',
        sourceKind: 'uploaded_evidence',
        includeInMerged: false,
        status: 'warning',
        generationError: error?.message || 'Evidence merge failed',
        sourceUploadId: evidence.upload.id || null,
      });
    }
  }

  return {
    bundleBytes: await bundlePdf.save(),
    bundleArtifacts: artifacts,
    warnings,
  };
}

async function createZipBundle(
  docs: Array<Pick<Section13PersistableDocument, 'file_name' | 'pdf'>>,
  evidenceFiles: Section13EvidenceFile[]
): Promise<Uint8Array> {
  const zip = new JSZip();

  for (const doc of docs) {
    zip.file(doc.file_name, doc.pdf);
  }

  for (const evidence of evidenceFiles) {
    zip.file(`uploaded-evidence/${evidence.upload.fileName}`, Buffer.from(evidence.bytes));
  }

  return zip.generateAsync({ type: 'uint8array' });
}

export async function generateSection13PreviewableDocument(params: {
  caseId: string;
  documentType: Section13PreviewableDocumentType;
  productType: Section13ProductSku;
  state: Section13State;
  comparables: Section13Comparable[];
  evidenceFiles?: Section13EvidenceFile[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13PersistableDocument> {
  const {
    caseId,
    documentType,
    productType,
    state,
    comparables,
    evidenceFiles = [],
    snapshot,
  } = params;
  const resolvedState = getSnapshotState(state, snapshot);
  const resolvedComparables = getSnapshotComparables(comparables, snapshot);

  if (
    productType !== 'section13_defensive' &&
    !SECTION13_CORE_DOCUMENT_TYPES.includes(
      documentType as (typeof SECTION13_CORE_DOCUMENT_TYPES)[number]
    )
  ) {
    throw new Error(`Document type ${documentType} requires the Section 13 Defensive Pack`);
  }

  switch (documentType) {
    case 'section13_form_4a': {
      const pdfBytes = await generateSection13Form4A(resolvedState, snapshot);
      return {
        title: 'Form 4A rent increase notice',
        description: 'Official Form 4A completed from your Section 13 wizard answers.',
        category: 'notice',
        document_type: 'section13_form_4a',
        file_name: `section13-form-4a-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_justification_report': {
      const pdfBytes = await buildJustificationReportPdf(
        resolvedState,
        resolvedComparables,
        snapshot
      );
      return {
        title: 'Rent increase justification report',
        description: 'Comparable rent evidence and adjusted market analysis.',
        category: 'evidence',
        document_type: 'section13_justification_report',
        file_name: `section13-justification-report-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_proof_of_service_record': {
      const pdfBytes = await generateProofOfServicePDF({
        landlord_name: resolvedState.landlord.landlordName,
        tenant_name: resolvedState.tenancy.tenantNames.join(', '),
        property_address: buildPropertyAddress(resolvedState),
        document_served: "Form 4A - Landlord's notice proposing a new rent",
        served_date: resolvedState.proposal.serviceDate || undefined,
        expiry_date: resolvedState.proposal.proposedStartDate || undefined,
        service_address: buildPropertyAddress(resolvedState),
        service_method:
          resolvedState.proposal.serviceMethod === 'hand_delivered'
            ? 'hand_delivery'
            : resolvedState.proposal.serviceMethod === 'post'
              ? 'first_class_post'
              : resolvedState.proposal.serviceMethod === 'registered_post'
                ? 'recorded_delivery'
                : resolvedState.proposal.serviceMethod === 'email'
                  ? 'email'
                  : undefined,
        footer_branding: SECTION13_PROOF_OF_SERVICE_BRAND,
      });
      return {
        title: 'Proof of service record',
        description: 'Service method record for the Section 13 notice.',
        category: 'evidence',
        document_type: 'section13_proof_of_service_record',
        file_name: `section13-proof-of-service-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_cover_letter': {
      const pdfBytes = await buildCoverLetterPdf(resolvedState, snapshot);
      return {
        title: 'Rent increase cover letter',
        description: 'Plain-English cover letter to accompany the notice.',
        category: 'guidance',
        document_type: 'section13_cover_letter',
        file_name: `section13-cover-letter-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_tribunal_argument_summary': {
      const pdfBytes = await buildTribunalArgumentSummaryPdf(
        resolvedState,
        resolvedComparables,
        snapshot
      );
      return {
        title: 'Tribunal Argument Summary',
        description:
          'One-page case-specific summary of the landlord position and consistent argument points.',
        category: 'guidance',
        document_type: 'section13_tribunal_argument_summary',
        file_name: `section13-tribunal-argument-summary-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_tribunal_defence_guide': {
      const pdfBytes = await buildDefenceGuidePdf(resolvedState);
      return {
        title: 'Tribunal defence guide',
        description: 'Step-by-step preparation guide for a challenged rent increase.',
        category: 'guidance',
        document_type: 'section13_tribunal_defence_guide',
        file_name: `section13-tribunal-defence-guide-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_landlord_response_template': {
      const pdfBytes = await buildLandlordResponseTemplatePdf(resolvedState);
      return {
        title: 'Landlord response template',
        description: 'Template response aligned to the current rent determination process.',
        category: 'court_form',
        document_type: 'section13_landlord_response_template',
        file_name: `section13-landlord-response-template-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
    case 'section13_legal_briefing': {
      const pdfBytes = await buildLegalBriefingPdf(resolvedState);
      return {
        title: 'Tribunal legal briefing',
        description: 'Short legal and practical hearing briefing.',
        category: 'guidance',
        document_type: 'section13_legal_briefing',
        file_name: `section13-legal-briefing-${caseId}.pdf`,
        pdf: Buffer.from(pdfBytes),
      };
    }
  }
}

export async function generateSection13CoreDocuments(params: {
  caseId: string;
  productType: Section13ProductSku;
  state: Section13State;
  comparables: Section13Comparable[];
  evidenceFiles?: Section13EvidenceFile[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13PersistableDocument[]> {
  const { caseId, productType, state, comparables, evidenceFiles = [], snapshot } = params;
  const documentTypes =
    productType === 'section13_defensive'
      ? SECTION13_DEFENSIVE_DOCUMENT_TYPES
      : SECTION13_CORE_DOCUMENT_TYPES;
  const documents = await Promise.all(
    documentTypes.map((documentType) =>
      generateSection13PreviewableDocument({
        caseId,
        documentType,
        productType,
        state,
        comparables,
        evidenceFiles,
        snapshot,
      })
    )
  );

  return [...documents].sort((left, right) => {
    const leftIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(left.document_type as any);
    const rightIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(right.document_type as any);
    return (leftIndex === -1 ? Number.MAX_SAFE_INTEGER : leftIndex) - (rightIndex === -1 ? Number.MAX_SAFE_INTEGER : rightIndex);
  });
}

async function buildSection13BundleDocuments(params: {
  caseId: string;
  productType: Extract<Section13ProductSku, 'section13_defensive'>;
  state: Section13State;
  evidenceFiles?: Section13EvidenceFile[];
  sourceDocuments: Section13BundleSourceDocument[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedBundle> {
  const { caseId, productType, state, evidenceFiles = [], sourceDocuments, snapshot } = params;
  const resolvedState = getSnapshotState(state, snapshot);
  const bundleWarnings: string[] = [];
  const sortedSourceDocuments = [...sourceDocuments].sort((left, right) => {
    const leftIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(left.document_type as any);
    const rightIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(right.document_type as any);
    return (leftIndex === -1 ? Number.MAX_SAFE_INTEGER : leftIndex) - (rightIndex === -1 ? Number.MAX_SAFE_INTEGER : rightIndex);
  });

  const coverPdf = await createBundleCoverPdf(resolvedState, productType);
  const docArtifacts = await Promise.all(
    sortedSourceDocuments.map(async (doc): Promise<BundleArtifact> => ({
      title: doc.title,
      logicalSection:
        doc.document_type === 'section13_form_4a'
          ? ('core_notice' as const)
          : ('supporting_report' as const),
      assetType:
        doc.document_type === 'section13_form_4a'
          ? ('form' as const)
          : ('report' as const),
      sourceKind: 'generated_document' as const,
      includeInMerged: true,
      status: 'generated' as const,
      pageCount: await pageCount(doc.pdf),
      documentType: doc.document_type,
      sourceDocumentId: doc.sourceDocumentId || null,
      exhibitLabel: null,
    }))
  );

  const coverArtifact: BundleArtifact = {
    title: 'Bundle cover sheet',
    logicalSection: 'cover',
    assetType: 'report',
    sourceKind: 'supporting_input',
    includeInMerged: true,
    status: 'generated',
    pageCount: await pageCount(coverPdf),
  };

  const exhibitArtifacts = [
    ...docArtifacts,
    ...evidenceFiles.map((item) => ({
      title: item.upload.title || item.upload.fileName,
      logicalSection: 'uploaded_evidence' as const,
      assetType: 'evidence' as const,
      sourceKind: 'uploaded_evidence' as const,
      includeInMerged: true,
      status: item.upload.uploadStatus === 'failed' ? ('warning' as const) : ('pending' as const),
      sourceUploadId: item.upload.id || null,
      exhibitLabel: item.upload.exhibitLabel || null,
    })),
  ].map((item, index) => ({
    ...item,
    exhibitLabel: item.exhibitLabel || `Exhibit ${String.fromCharCode(65 + index)}`,
  }));

  const indexPdf = await createBundleIndexPdf([coverArtifact, ...exhibitArtifacts]);
  const { bundleBytes, bundleArtifacts, warnings } = await mergePdfArtifacts(
    coverPdf,
    indexPdf,
    sortedSourceDocuments,
    evidenceFiles
  );

  bundleWarnings.push(...warnings);

  const bundlePdfDocument: Section13PersistableDocument = {
    title: 'Merged tribunal bundle PDF',
    description: 'Indexed hearing bundle with exhibits and supporting documents.',
    category: 'evidence',
    document_type: 'section13_tribunal_bundle',
    file_name: `section13-tribunal-bundle-${caseId}.pdf`,
    pdf: Buffer.from(bundleBytes),
  };

  const zipBytes = await createZipBundle([...sortedSourceDocuments, bundlePdfDocument], evidenceFiles);
  const zipDocument: Section13PersistableDocument = {
    title: 'Tribunal bundle ZIP',
    description: 'ZIP export containing the merged bundle and all supporting files.',
    category: 'evidence',
    document_type: 'section13_tribunal_bundle_zip',
    file_name: `section13-tribunal-bundle-${caseId}.zip`,
    pdf: Buffer.from(zipBytes),
    contentType: 'application/zip',
  };

  const bundleSeeds: BundleArtifact[] = [
    coverArtifact,
    {
      title: 'Bundle index',
      logicalSection: 'index',
      assetType: 'report',
      sourceKind: 'supporting_input',
      includeInMerged: true,
      status: 'generated',
      pageCount: await pageCount(indexPdf),
    },
    ...bundleArtifacts
      .filter((item) => item.logicalSection !== 'cover' && item.logicalSection !== 'index')
      .map((item) => ({
        ...item,
        exhibitLabel: item.exhibitLabel || null,
      })),
  ];

  return {
    documents: [bundlePdfDocument, zipDocument],
    bundleAssets: bundleSeeds.map((item, index) => ({
      rulesVersion: resolvedState.rulesVersion,
      logicalSection: item.logicalSection,
      assetType: item.assetType,
      title: item.title,
      sourceKind: item.sourceKind,
      exhibitLabel: item.exhibitLabel || null,
      orderIndex: index,
      includeInMerged: item.includeInMerged,
      status: item.status,
      pageCount: item.pageCount ?? null,
      generationError: item.generationError ?? null,
      sourceDocumentId: item.sourceDocumentId || null,
      sourceUploadId: item.sourceUploadId || null,
      metadata: {
        document_type: item.documentType || null,
        ...(item.metadata || {}),
      },
    })),
    workflowStatus: bundleWarnings.length > 0 ? 'bundle_partial_warning' : 'bundle_ready',
    bundleWarnings,
  };
}

export async function generateSection13TribunalBundle(params: {
  caseId: string;
  state: Section13State;
  evidenceFiles?: Section13EvidenceFile[];
  sourceDocuments: Section13BundleSourceDocument[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedBundle> {
  return buildSection13BundleDocuments({
    ...params,
    productType: 'section13_defensive',
  });
}

export async function generateSection13Pack(params: {
  caseId: string;
  productType: Section13ProductSku;
  state: Section13State;
  comparables: Section13Comparable[];
  evidenceFiles?: Section13EvidenceFile[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedPack> {
  const { caseId, productType, state, comparables, evidenceFiles = [], snapshot } = params;
  const resolvedState = getSnapshotState(state, snapshot);
  const documents = await generateSection13CoreDocuments({
    caseId,
    productType,
    state: resolvedState,
    comparables,
    evidenceFiles,
    snapshot,
  });
  const bundleWarnings: string[] = [];
  const bundleAssets: Section13BundleAsset[] = [];
  let workflowStatus: Section13WorkflowStatus = 'fulfilled';

  if (productType === 'section13_defensive') {
    workflowStatus = 'fulfilled';
  }

  if (resolvedState.preview?.warnings?.length) {
    bundleWarnings.push(...resolvedState.preview.warnings);
  }

  return {
    documents,
    bundleAssets,
    workflowStatus,
    bundleWarnings,
  };
}
