  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.jointSignatory2, '');
  setEditableTextFieldValue(form, FORM_4A_OFFICIAL_FIELD_NAMES.text.jointSignatory3, '');
