function wrapText(text: string, font: PDFFont, fontSize: number, maxWidth: number): string[] {
  const paragraphs = safeText(text).split('\n');
  const lines: string[] = [];

  for (const paragraph of paragraphs) {
    const words = paragraph.split(/\s+/).filter(Boolean);
    if (words.length === 0) {
      lines.push('');
      continue;
    }

    let currentLine = words[0];
    for (let index = 1; index < words.length; index += 1) {
      const nextLine = `${currentLine} ${words[index]}`;
      if (font.widthOfTextAtSize(nextLine, fontSize) <= maxWidth) {
        currentLine = nextLine;
      } else {
        lines.push(currentLine);
        currentLine = words[index];
      }
    }
    lines.push(currentLine);
  }

  return lines;
}

function drawWrappedText(params: {
  page: PDFPage;
  text: string;
  x: number;
  y: number;
  width: number;
  height?: number;
  font: PDFFont;
  fontSize?: number;
  lineHeight?: number;
  align?: 'left' | 'center' | 'right';
}): void {
  const {
    page,
    text,
    x,
    y,
    width,
    height = Number.POSITIVE_INFINITY,
    font,
    fontSize = DEFAULT_FONT_SIZE,
    lineHeight = fontSize + 2,
    align = 'left',
  } = params;

  const lines = wrapText(text, font, fontSize, width);
  let cursorY = y;

  for (const line of lines) {
    if (cursorY < y - height + lineHeight) {
      break;
    }
    const safeLine = safeText(line);
    const textWidth = font.widthOfTextAtSize(safeLine, fontSize);
    const drawX =
      align === 'center'
        ? x + Math.max((width - textWidth) / 2, 0)
        : align === 'right'
          ? x + Math.max(width - textWidth, 0)
          : x;
    page.drawText(safeText(line), {
      x: drawX,
      y: cursorY,
      font,
      size: fontSize,
      color: rgb(0.12, 0.12, 0.12),
    });
    cursorY -= lineHeight;
  }
}

function drawTextBox(params: {
  page: PDFPage;
  text: string;
  x: number;
  y: number;
  width: number;
  height: number;
  font: PDFFont;
  fontSize?: number;
  align?: 'left' | 'center' | 'right';
}): void {
  drawWrappedText({
    page: params.page,
    text: params.text,
    x: params.x,
    y: params.y + params.height - (params.fontSize || DEFAULT_FONT_SIZE) - 1,
    width: params.width,
    height: params.height,
    font: params.font,
    fontSize: params.fontSize,
    align: params.align,
  });
}

