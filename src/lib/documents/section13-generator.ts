async function createNarrativePdf(title: string, blocks: SectionBlock[], footer?: string): Promise<Uint8Array> {
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const bold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  let page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
  let cursorY = PAGE_HEIGHT - MARGIN_Y;

  const addPage = () => {
    page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
    cursorY = PAGE_HEIGHT - MARGIN_Y;
  };

  const ensureSpace = (needed: number) => {
    if (cursorY - needed < MARGIN_Y) {
      addPage();
    }
  };

  page.drawText(safeText(SECTION13_BRAND_LABEL), {
    x: MARGIN_X,
    y: cursorY,
    font,
    size: 8,
    color: rgb(0.42, 0.42, 0.46),
  });
  cursorY -= 16;

  page.drawText(safeText(title), {
    x: MARGIN_X,
    y: cursorY,
    font: bold,
    size: 20,
    color: rgb(0.12, 0.12, 0.12),
  });
  cursorY -= 14;
  page.drawLine({
    start: { x: MARGIN_X, y: cursorY },
    end: { x: PAGE_WIDTH - MARGIN_X, y: cursorY },
    thickness: 1,
    color: rgb(0.84, 0.84, 0.88),
  });
  cursorY -= 18;

  for (const [blockIndex, block] of blocks.entries()) {
    const blockFontSize = block.variant === 'intro' ? 10.5 : DEFAULT_FONT_SIZE;
    const blockLineHeight = block.variant === 'intro' ? 13 : LINE_HEIGHT;
    const paragraphHeights = block.lines.map((paragraph) => {
      const wrapped = wrapText(
        paragraph,
        font,
        blockFontSize,
        CONTENT_WIDTH - (block.variant === 'intro' || block.variant === 'callout' ? 28 : 0)
      );
      return wrapped.length * blockLineHeight + 6;
    });
    const totalParagraphHeight = paragraphHeights.reduce((sum, value) => sum + value, 0);

    if (block.heading && block.variant !== 'callout') {
      ensureSpace(34);
      page.drawText(safeText(block.heading), {
        x: MARGIN_X,
        y: cursorY,
        font: bold,
        size: 12.5,
        color: rgb(0.16, 0.16, 0.16),
      });
      const headingWidth = bold.widthOfTextAtSize(safeText(block.heading), 12.5);
      page.drawLine({
        start: { x: MARGIN_X + headingWidth + 12, y: cursorY + 6 },
        end: { x: PAGE_WIDTH - MARGIN_X, y: cursorY + 6 },
        thickness: 0.7,
        color: rgb(0.86, 0.86, 0.9),
      });
      cursorY -= 22;
    }

    if (block.variant === 'intro' || block.variant === 'callout') {
      const panelPaddingX = 14;
      const panelPaddingY = 12;
      const headingHeight = block.variant === 'callout' && block.heading ? 22 : 0;
      const panelHeight = totalParagraphHeight + panelPaddingY * 2 + headingHeight;
      ensureSpace(panelHeight + 10);
      page.drawRectangle({
        x: MARGIN_X,
        y: cursorY - panelHeight + 6,
        width: CONTENT_WIDTH,
        height: panelHeight,
        color: block.variant === 'intro' ? rgb(0.965, 0.968, 0.978) : rgb(0.973, 0.963, 0.992),
        borderColor: block.variant === 'intro' ? rgb(0.84, 0.86, 0.9) : rgb(0.86, 0.82, 0.95),
        borderWidth: 0.8,
      });
      cursorY -= panelPaddingY;
      if (block.variant === 'callout' && block.heading) {
        page.drawText(safeText(block.heading), {
          x: MARGIN_X + panelPaddingX,
          y: cursorY,
          font: bold,
          size: 12,
          color: rgb(0.16, 0.16, 0.16),
        });
        cursorY -= 20;
      }
      for (const paragraph of block.lines) {
        const wrapped = wrapText(paragraph, font, blockFontSize, CONTENT_WIDTH - panelPaddingX * 2);
        ensureSpace(wrapped.length * blockLineHeight + 8);
        for (const line of wrapped) {
          page.drawText(safeText(line), {
            x: MARGIN_X + panelPaddingX,
            y: cursorY,
            font,
            size: blockFontSize,
            color: rgb(0.18, 0.18, 0.18),
          });
          cursorY -= blockLineHeight;
        }
        cursorY -= 6;
      }
      cursorY -= panelPaddingY - 6;
      continue;
    }

    for (const paragraph of block.lines) {
      const wrapped = wrapText(paragraph, font, blockFontSize, CONTENT_WIDTH);
      ensureSpace(wrapped.length * blockLineHeight + 8);
      for (const line of wrapped) {
        page.drawText(safeText(line), {
          x: MARGIN_X,
          y: cursorY,
          font,
          size: blockFontSize,
          color: rgb(0.18, 0.18, 0.18),
        });
        cursorY -= blockLineHeight;
      }
      cursorY -= 6;
    }

    if (blockIndex < blocks.length - 1) {
      cursorY -= 4;
    }
  }

  if (footer) {
    const pages = pdfDoc.getPages();
    for (const [index, item] of pages.entries()) {
      item.drawLine({
        start: { x: MARGIN_X, y: 36 },
        end: { x: PAGE_WIDTH - MARGIN_X, y: 36 },
        thickness: 0.5,
        color: rgb(0.8, 0.8, 0.8),
      });
      item.drawText(safeText(footer), {
        x: MARGIN_X,
        y: 20,
        font,
        size: 8,
        color: rgb(0.45, 0.45, 0.45),
      });
      const pageNumber = `Page ${index + 1} of ${pages.length}`;
      item.drawText(pageNumber, {
        x: PAGE_WIDTH - MARGIN_X - font.widthOfTextAtSize(pageNumber, 8),
        y: 20,
        font,
        size: 8,
        color: rgb(0.45, 0.45, 0.45),
      });
    }
  }

  return pdfDoc.save();
}

