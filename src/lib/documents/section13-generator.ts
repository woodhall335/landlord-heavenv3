async function createNarrativePdf(title: string, blocks: SectionBlock[], footer?: string): Promise<Uint8Array> {
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const bold = await pdfDoc.embedFont(StandardFonts.HelveticaBold);
  const logoBytes = await getBrandLogoBytes();
  const logoImage = logoBytes ? await pdfDoc.embedPng(logoBytes) : null;
  let page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
  let cursorY = PAGE_HEIGHT - MARGIN_Y;
  const titleFontSize = 18;
  const headingFontSize = 12;
  const bodyFontSize = 10.5;
  const bodyLineHeight = 13.5;
  const brandText = rgb(0.204, 0.141, 0.373);
  const brandTextStrong = rgb(0.141, 0.09, 0.247);
  const brandMuted = rgb(0.424, 0.365, 0.561);
  const brandSurface = rgb(0.965, 0.945, 1);
  const brandSurfaceStrong = rgb(0.929, 0.894, 1);
  const brandPanel = rgb(0.984, 0.98, 1);
  const brandBorder = rgb(0.847, 0.8, 0.965);
  const brandBorderStrong = rgb(0.776, 0.714, 0.937);

  const drawHeader = () => {
    let headerY = PAGE_HEIGHT - MARGIN_Y;

    if (logoImage) {
      const scaled = logoImage.scaleToFit(168, 34);
      page.drawImage(logoImage, {
        x: MARGIN_X,
        y: headerY - scaled.height + 2,
        width: scaled.width,
        height: scaled.height,
      });
      headerY -= scaled.height + 10;
    } else {
      page.drawText(safeText(SECTION13_BRAND_LABEL), {
        x: MARGIN_X,
        y: headerY,
        font: bold,
        size: 10,
        color: brandText,
      });
      headerY -= 16;
    }

    page.drawText(safeText(title), {
      x: MARGIN_X,
      y: headerY,
      font: bold,
      size: titleFontSize,
      color: brandTextStrong,
    });
    headerY -= 12;

    page.drawLine({
      start: { x: MARGIN_X, y: headerY },
      end: { x: PAGE_WIDTH - MARGIN_X, y: headerY },
      thickness: 1,
      color: brandBorder,
    });

    cursorY = headerY - 14;
  };

  const addPage = () => {
    page = pdfDoc.addPage([PAGE_WIDTH, PAGE_HEIGHT]);
    drawHeader();
  };

  const ensureSpace = (needed: number) => {
    if (cursorY - needed < MARGIN_Y) {
      addPage();
    }
  };

  drawHeader();

  for (const [blockIndex, block] of blocks.entries()) {
    const blockFontSize = block.variant === 'intro' ? bodyFontSize : bodyFontSize;
    const blockLineHeight = block.variant === 'intro' ? bodyLineHeight : bodyLineHeight;
    const paragraphHeights = block.lines.map((paragraph) => {
      const wrapped = wrapText(
        paragraph,
        font,
        blockFontSize,
        CONTENT_WIDTH - (block.variant === 'intro' || block.variant === 'callout' ? 28 : 0)
      );
      return wrapped.length * blockLineHeight + 6;
    });
    const totalParagraphHeight = paragraphHeights.reduce((sum, value) => sum + value, 0);

    if (block.heading && block.variant !== 'callout') {
      ensureSpace(28);
      page.drawText(safeText(block.heading), {
        x: MARGIN_X,
        y: cursorY,
        font: bold,
        size: headingFontSize,
        color: brandTextStrong,
      });
      const headingWidth = bold.widthOfTextAtSize(safeText(block.heading), headingFontSize);
      page.drawLine({
        start: { x: MARGIN_X + headingWidth + 12, y: cursorY + 6 },
        end: { x: PAGE_WIDTH - MARGIN_X, y: cursorY + 6 },
        thickness: 0.7,
        color: brandBorder,
      });
      cursorY -= 18;
    }

    if (block.variant === 'intro' || block.variant === 'callout') {
      const panelPaddingX = 12;
      const panelPaddingY = 10;
      const headingHeight = block.variant === 'callout' && block.heading ? 22 : 0;
      const panelHeight = totalParagraphHeight + panelPaddingY * 2 + headingHeight;
      ensureSpace(panelHeight + 8);
      page.drawRectangle({
        x: MARGIN_X,
        y: cursorY - panelHeight + 6,
        width: CONTENT_WIDTH,
        height: panelHeight,
        color: block.variant === 'intro' ? brandSurface : brandSurfaceStrong,
        borderColor: block.variant === 'intro' ? brandBorder : brandBorderStrong,
        borderWidth: 1,
      });
      cursorY -= panelPaddingY;
      if (block.variant === 'callout' && block.heading) {
        page.drawText(safeText(block.heading), {
          x: MARGIN_X + panelPaddingX,
          y: cursorY,
          font: bold,
          size: 11.5,
          color: brandTextStrong,
        });
        cursorY -= 17;
      }
      for (const paragraph of block.lines) {
        const wrapped = wrapText(paragraph, font, blockFontSize, CONTENT_WIDTH - panelPaddingX * 2);
        ensureSpace(wrapped.length * blockLineHeight + 6);
        for (const line of wrapped) {
          page.drawText(safeText(line), {
            x: MARGIN_X + panelPaddingX,
            y: cursorY,
            font,
            size: blockFontSize,
            color: brandText,
          });
          cursorY -= blockLineHeight;
        }
        cursorY -= 4;
      }
      cursorY -= panelPaddingY - 6;
      continue;
    }

    for (const paragraph of block.lines) {
      const wrapped = wrapText(paragraph, font, blockFontSize, CONTENT_WIDTH);
      ensureSpace(wrapped.length * blockLineHeight + 6);
      for (const line of wrapped) {
        page.drawText(safeText(line), {
          x: MARGIN_X,
          y: cursorY,
          font,
          size: blockFontSize,
          color: brandText,
        });
        cursorY -= blockLineHeight;
      }
      cursorY -= 4;
    }

    if (blockIndex < blocks.length - 1) {
      cursorY -= 2;
    }
  }

  if (footer) {
    const pages = pdfDoc.getPages();
    for (const [index, item] of pages.entries()) {
      item.drawLine({
        start: { x: MARGIN_X, y: 36 },
        end: { x: PAGE_WIDTH - MARGIN_X, y: 36 },
        thickness: 0.5,
        color: brandBorder,
      });
      item.drawText(safeText(footer), {
        x: MARGIN_X,
        y: 20,
        font,
        size: 8,
        color: brandMuted,
      });
      const pageNumber = `Page ${index + 1} of ${pages.length}`;
      item.drawText(pageNumber, {
        x: PAGE_WIDTH - MARGIN_X - font.widthOfTextAtSize(pageNumber, 8),
        y: 20,
        font,
        size: 8,
        color: brandMuted,
      });
    }
  }

  return pdfDoc.save();
}

