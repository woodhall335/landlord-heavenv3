function frequencyLabel(frequency: string): string {
  switch (frequency) {
    case 'weekly':
      return 'per week';
    case 'fortnightly':
      return 'per fortnight';
    case '4-weekly':
      return 'per four weeks';
    case 'monthly':
    case 'pcm':
      return 'per month';
    case 'ppw':
      return 'per week';
    default:
      return frequency;
  }
}

