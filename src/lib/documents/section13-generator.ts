export async function generateSection13CoreDocuments(params: {
  caseId: string;
  productType: Section13ProductSku;
  state: Section13State;
  comparables: Section13Comparable[];
  evidenceFiles?: Section13EvidenceFile[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13PersistableDocument[]> {
  const { caseId, productType, state, comparables, evidenceFiles = [], snapshot } = params;
  const documentTypes =
    productType === 'section13_defensive'
      ? SECTION13_DEFENSIVE_DOCUMENT_TYPES
      : SECTION13_CORE_DOCUMENT_TYPES;
  const documents = await Promise.all(
    documentTypes.map((documentType) =>
      generateSection13PreviewableDocument({
        caseId,
        documentType,
        productType,
        state,
        comparables,
        evidenceFiles,
        snapshot,
      })
    )
  );

  return [...documents].sort((left, right) => {
    const leftIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(left.document_type as any);
    const rightIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(right.document_type as any);
    return (leftIndex === -1 ? Number.MAX_SAFE_INTEGER : leftIndex) - (rightIndex === -1 ? Number.MAX_SAFE_INTEGER : rightIndex);
  });
}

async function buildSection13BundleDocuments(params: {
  caseId: string;
  productType: Extract<Section13ProductSku, 'section13_defensive'>;
  state: Section13State;
  evidenceFiles?: Section13EvidenceFile[];
  sourceDocuments: Section13BundleSourceDocument[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedBundle> {
  const { caseId, productType, state, evidenceFiles = [], sourceDocuments, snapshot } = params;
  const resolvedState = getSnapshotState(state, snapshot);
  const bundleWarnings: string[] = [];
  const sortedSourceDocuments = [...sourceDocuments].sort((left, right) => {
    const leftIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(left.document_type as any);
    const rightIndex = SECTION13_BUNDLE_SOURCE_DOCUMENT_TYPES.indexOf(right.document_type as any);
    return (leftIndex === -1 ? Number.MAX_SAFE_INTEGER : leftIndex) - (rightIndex === -1 ? Number.MAX_SAFE_INTEGER : rightIndex);
  });

  const coverPdf = await createBundleCoverPdf(resolvedState, productType);
  const docArtifacts = await Promise.all(
    sortedSourceDocuments.map(async (doc): Promise<BundleArtifact> => ({
      title: doc.title,
      logicalSection:
        doc.document_type === 'section13_form_4a'
          ? ('core_notice' as const)
          : doc.document_type === 'section13_negotiation_email_template'
            ? ('correspondence' as const)
            : ('supporting_report' as const),
      assetType:
        doc.document_type === 'section13_form_4a'
          ? ('form' as const)
          : doc.document_type === 'section13_negotiation_email_template'
            ? ('correspondence' as const)
            : ('report' as const),
      sourceKind: 'generated_document' as const,
      includeInMerged: true,
      status: 'generated' as const,
      pageCount: await pageCount(doc.pdf),
      documentType: doc.document_type,
      sourceDocumentId: doc.sourceDocumentId || null,
      exhibitLabel: null,
    }))
  );

  const coverArtifact: BundleArtifact = {
    title: 'Bundle cover sheet',
    logicalSection: 'cover',
    assetType: 'report',
    sourceKind: 'supporting_input',
    includeInMerged: true,
    status: 'generated',
    pageCount: await pageCount(coverPdf),
  };

  const exhibitArtifacts = [
    ...docArtifacts,
    ...evidenceFiles.map((item) => ({
      title: item.upload.title || item.upload.fileName,
      logicalSection: 'uploaded_evidence' as const,
      assetType: 'evidence' as const,
      sourceKind: 'uploaded_evidence' as const,
      includeInMerged: true,
      status: item.upload.uploadStatus === 'failed' ? ('warning' as const) : ('pending' as const),
      sourceUploadId: item.upload.id || null,
      exhibitLabel: item.upload.exhibitLabel || null,
    })),
  ].map((item, index) => ({
    ...item,
    exhibitLabel: item.exhibitLabel || `Exhibit ${String.fromCharCode(65 + index)}`,
  }));

  const indexPdf = await createBundleIndexPdf([coverArtifact, ...exhibitArtifacts]);
  const { bundleBytes, bundleArtifacts, warnings } = await mergePdfArtifacts(
    coverPdf,
    indexPdf,
    sortedSourceDocuments,
    evidenceFiles
  );

  bundleWarnings.push(...warnings);

  const bundlePdfDocument: Section13PersistableDocument = {
    title: 'Merged tribunal bundle PDF',
    description: 'Indexed hearing bundle with exhibits and supporting documents.',
    document_type: 'section13_tribunal_bundle',
    file_name: `section13-tribunal-bundle-${caseId}.pdf`,
    pdf: Buffer.from(bundleBytes),
  };

  const zipBytes = await createZipBundle([...sortedSourceDocuments, bundlePdfDocument], evidenceFiles);
  const zipDocument: Section13PersistableDocument = {
    title: 'Tribunal bundle ZIP',
    description: 'ZIP export containing the merged bundle and all supporting files.',
    document_type: 'section13_tribunal_bundle_zip',
    file_name: `section13-tribunal-bundle-${caseId}.zip`,
    pdf: Buffer.from(zipBytes),
    contentType: 'application/zip',
  };

  const bundleSeeds: BundleArtifact[] = [
    coverArtifact,
    {
      title: 'Bundle index',
      logicalSection: 'index',
      assetType: 'report',
      sourceKind: 'supporting_input',
      includeInMerged: true,
      status: 'generated',
      pageCount: await pageCount(indexPdf),
    },
    ...bundleArtifacts
      .filter((item) => item.logicalSection !== 'cover' && item.logicalSection !== 'index')
      .map((item) => ({
        ...item,
        exhibitLabel: item.exhibitLabel || null,
      })),
  ];

  return {
    documents: [bundlePdfDocument, zipDocument],
    bundleAssets: bundleSeeds.map((item, index) => ({
      rulesVersion: resolvedState.rulesVersion,
      logicalSection: item.logicalSection,
      assetType: item.assetType,
      title: item.title,
      sourceKind: item.sourceKind,
      exhibitLabel: item.exhibitLabel || null,
      orderIndex: index,
      includeInMerged: item.includeInMerged,
      status: item.status,
      pageCount: item.pageCount ?? null,
      generationError: item.generationError ?? null,
      sourceDocumentId: item.sourceDocumentId || null,
      sourceUploadId: item.sourceUploadId || null,
      metadata: {
        document_type: item.documentType || null,
        ...(item.metadata || {}),
      },
    })),
    workflowStatus: bundleWarnings.length > 0 ? 'bundle_partial_warning' : 'bundle_ready',
    bundleWarnings,
  };
}

export async function generateSection13TribunalBundle(params: {
  caseId: string;
  state: Section13State;
  evidenceFiles?: Section13EvidenceFile[];
  sourceDocuments: Section13BundleSourceDocument[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedBundle> {
  return buildSection13BundleDocuments({
    ...params,
    productType: 'section13_defensive',
  });
}

export async function generateSection13Pack(params: {
  caseId: string;
  productType: Section13ProductSku;
  state: Section13State;
  comparables: Section13Comparable[];
  evidenceFiles?: Section13EvidenceFile[];
  snapshot?: Section13OutputSnapshot | null;
}): Promise<Section13GeneratedPack> {
  const { caseId, productType, state, comparables, evidenceFiles = [], snapshot } = params;
  const resolvedState = getSnapshotState(state, snapshot);
  const documents = await generateSection13CoreDocuments({
    caseId,
    productType,
    state: resolvedState,
    comparables,
    evidenceFiles,
    snapshot,
  });
  const bundleWarnings: string[] = [];
  const bundleAssets: Section13BundleAsset[] = [];
  let workflowStatus: Section13WorkflowStatus = 'fulfilled';

  if (productType === 'section13_defensive') {
    workflowStatus = 'fulfilled';
  }

  if (resolvedState.preview?.warnings?.length) {
    bundleWarnings.push(...resolvedState.preview.warnings);
  }

  return {
    documents,
    bundleAssets,
    workflowStatus,
    bundleWarnings,
  };
}
